# Make a dictionary to store information about a person.
person = {
    'first_name': 'Aben',
    'last_name': 'Abey',
    'age': 19,
    'city': 'Abu Shagara'
}

# Print the information stored in the dictionary.
print("First name: " + person['first_name'])
print("Last name: " + person['last_name'])
print("Age: " + str(person['age']))
print("City: " + person['city'])